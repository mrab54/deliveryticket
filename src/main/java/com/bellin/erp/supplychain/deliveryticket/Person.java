package com.bellin.erp.supplychain.deliveryticket;

public class Person {

    private String name;
    private int width;

    public Person(String name, int width) {
        this.name = name;
        this.width = width;
    }

    public String getName() {
        return this.name;
    }
    public int getWidth() {
        return this.width;
    }
}
